package coreJava;

public class Patterns {

	public static void main(String[] args) {
		for(int i = 5;i>=1;i--) {
			for(int j=1; j<=i; j++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		System.out.println("...............");
		
		
		for(int l=1;l<=5;l++) {
			for(int m = 1 ; m <= l ; m++) {
				System.out.print("* ");
			}
			System.out.println();
			
		}


	

}}
