package collectionFramework;

public class Autoboxing_unbox {
	public static void main(String[] args) {
		//autoboxing
		
		int a = 28;
		System.out.println(a);
		
		Integer b = new Integer(28);
		System.out.println(b);
		
		System.out.println("................");
		double d = 4.5;
		Double e = new Double(d);
		System.out.println(d +","+e);
		
	}
	

}
